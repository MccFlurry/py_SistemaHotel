    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet rsEmpresa = objEmpresa.buscarEmpresaPorDocumento(txtClienteid.getText());
            if (rsEmpresa.next()) {
                txtRazonSocial.setText(rsEmpresa.getString("razon_social"));
                int tipoId = rsEmpresa.getInt("tipo_id");

                // Seleccionar el tipo de empresa adecuado en el ComboBox
                for (int i = 0; i < cbTipoEmpresa.getItemCount(); i++) {
                    if (obtenerIdTipoEmpresa((String) cbTipoEmpresa.getItemAt(i)) == tipoId) {
                        cbTipoEmpresa.setSelectedIndex(i);
                        break;
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Empresa no encontrada.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error de SQL: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar empresa: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                btnNuevo.setText("Guardar");
                limpiarControles();
                txtClienteid.requestFocus();  // Aquí debería ser el número de documento, no cliente_id
            } else {
                btnNuevo.setText("Nuevo");
                String tipoEmpresa = (String) cbTipoEmpresa.getSelectedItem();
                int tipoId = obtenerIdTipoEmpresa(tipoEmpresa);
                // Registra la empresa basándose en numero_documento
                objEmpresa.registrarEmpresaPorDocumento(txtClienteid.getText(), txtRazonSocial.getText(), tipoId);
                limpiarControles();
                listarEmpresas();  // Actualiza la tabla después de registrar
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar empresa: " + e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea eliminar esta empresa?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                // Eliminar basándose en numero_documento
                objEmpresa.eliminarEmpresaPorDocumento(txtClienteid.getText());
                limpiarControles();
                listarEmpresas();  // Actualiza la tabla después de eliminar
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar empresa: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

