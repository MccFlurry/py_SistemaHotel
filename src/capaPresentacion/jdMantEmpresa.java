    public void listarEmpresas() {
        ResultSet rsEmpresas = null;
        Vector registro;
        DefaultTableModel modelo = new DefaultTableModel();

        // Modificar las columnas para reflejar el numero_documento
        modelo.addColumn("Numero Documento");
        modelo.addColumn("Razón Social");
        modelo.addColumn("Tipo Empresa");

        try {
            rsEmpresas = objEmpresa.listarEmpresas();
            while (rsEmpresas.next()) {
                registro = new Vector();
                registro.add(rsEmpresas.getString("numero_documento")); // Mostrar numero_documento
                registro.add(rsEmpresas.getString("razon_social"));
                registro.add(rsEmpresas.getInt("tipo_id"));
                modelo.addRow(registro);
            }
            tblEmpresas.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    public void listarTiposEmpresa() {
        ResultSet rsTiposEmpresa = null;
        cbTipoEmpresa.removeAllItems();
        DefaultComboBoxModel modeloTp = new DefaultComboBoxModel();
        cbTipoEmpresa.setModel(modeloTp);

        try {
            rsTiposEmpresa = objEmpresa.listarTiposEmpresa();
            while (rsTiposEmpresa.next()) {
                modeloTp.addElement(rsTiposEmpresa.getString("nombre_tipo"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private int obtenerIdTipoEmpresa(String nombreTipo) throws Exception {
        ResultSet rsTipoEmpresa = objEmpresa.listarTiposEmpresa();
        while (rsTipoEmpresa.next()) {
            if (rsTipoEmpresa.getString("nombre_tipo").equals(nombreTipo)) {
                return rsTipoEmpresa.getInt("tipo_id");
            }
        }
        throw new Exception("Tipo de empresa no encontrado.");
    }

    private void limpiarControles() {
        txtClienteid.setText("");
        txtRazonSocial.setText("");
        cbTipoEmpresa.setSelectedIndex(-1);
        txtClienteid.requestFocus();
    }

