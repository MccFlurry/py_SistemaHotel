    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        try {
            String resultado = objUsuario.login(txtCorreo.getText(), txtContra.getText());
            if (resultado.equals("")) {
                JOptionPane.showMessageDialog(null, "Acceso denegado, intente nuevamente");
                intentos++;
                if (intentos >= 3) {
                    JOptionPane.showMessageDialog(null, "Super√≥ los 3 intentos. El sistema se cerrar√°");
                    System.exit(0);
                }
            } else {
                String[] partes = resultado.split(":");
                String tipoUsuario = partes[0];   // "empleado" o "cliente"
                String dniUsuario = partes[1];
                String nombreCompleto = partes[2];

                FrmPrincipal frmPrincipal = (FrmPrincipal) this.getParent();
                frmPrincipal.setUsuarioLogueado(nombreCompleto);  // Mostrar el nombre en la interfaz

                // Asignar el tipo de usuario al formulario principal
                frmPrincipal.setTipoUsuario(tipoUsuario);

                // Mostrar mensaje seg√∫n el tipo de usuario
                if (tipoUsuario.equals("empleado")) {
                    JOptionPane.showMessageDialog(null, "Bienvenido Empleado con DNI: " + dniUsuario);
                } else if (tipoUsuario.equals("cliente")) {
                    JOptionPane.showMessageDialog(null, "Bienvenido Cliente con DNI: " + dniUsuario);
                }

                this.dispose();  // Cerrar el cuadro de di√°logo de inicio de sesi√≥n
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

